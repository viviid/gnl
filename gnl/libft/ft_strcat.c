#include "includes/libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	int	i;
	int j;

	i = ft_strlen(dest);
	j = 0;
	if (dest == NULL || src == NULL)
		return (NULL);
	while (src[j] != '\0')
	{
		dest[i + j] = src[j];
		j++;
	}
	dest[i + j] = '\0';
	return (dest);
}
